from flask import Flask, render_template, request, jsonify, send_from_directory
import ollama
import os
from werkzeug.utils import secure_filename

app = Flask(__name__)

# Konfiguration für Bild-Uploads
UPLOAD_FOLDER = 'uploads'
ALLOWED_EXTENSIONS = {'png', 'jpg', 'jpeg', 'gif'}
app.config['UPLOAD_FOLDER'] = UPLOAD_FOLDER
app.config['UPLOAD_URL'] = '/uploads/'

# Sicherstellen, dass der Upload-Ordner existiert
if not os.path.exists(UPLOAD_FOLDER):
    os.makedirs(UPLOAD_FOLDER)

# Funktion, um Dateiendungen zu überprüfen
def allowed_file(filename):
    return '.' in filename and filename.rsplit('.', 1)[1].lower() in ALLOWED_EXTENSIONS

@app.route('/')
def index():
    return render_template('index6.html')

# Statische Route für den Upload-Ordner
@app.route('/uploads/<filename>')
def uploaded_file(filename):
    return send_from_directory(app.config['UPLOAD_FOLDER'], filename)

@app.route('/send_message', methods=['POST'])
def send_message():
    # Text und Bild vom Formular empfangen
    user_message = request.form.get('message', '')  # Die Benutzernachricht für das Bild
    if 'image' not in request.files:
        return jsonify({'error': 'No file part'}), 400
    file = request.files['image']
    if file.filename == '':
        return jsonify({'error': 'No selected file'}), 400
    if file and allowed_file(file.filename):
        filename = secure_filename(file.filename)
        filepath = os.path.join(app.config['UPLOAD_FOLDER'], filename)
        file.save(filepath)

        # Vision-Modell aufrufen mit Benutzertext und Bild
        try:
            response = ollama.chat(
                model='llava',
                messages=[{
                    'role': 'user',
                    'content': user_message,  # Textnachricht des Benutzers
                    'images': [filepath]       # Hochgeladenes Bild
                }]
            )
            response_content = response['message']['content']
            return jsonify({'response': response_content, 'image_url': app.config['UPLOAD_URL'] + filename})  # URL zurückgeben
        except Exception as e:
            return jsonify({'error': str(e)}), 500
    else:
        return jsonify({'error': 'Invalid file type'}), 400

if __name__ == "__main__":
    app.run(debug=True)
